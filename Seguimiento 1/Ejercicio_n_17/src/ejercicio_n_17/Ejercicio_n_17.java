package ejercicio_n_17;

public class Ejercicio_n_17 {

    public static void main(String[] args) {
        double radio, area, perimetro;
        radio = 15;
        area = Math.PI * Math.pow(radio, 2);
        perimetro = 2 * Math.PI * radio;
        System.out.println("Para el radio: " + radio);
        System.out.println("Se tiene el area: " + area);
        System.out.println("Y el perimetro: " + perimetro);
    }
    
}
